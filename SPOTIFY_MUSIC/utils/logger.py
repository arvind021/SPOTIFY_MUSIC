from pyrogram.enums import ParseMode

from SPOTIFY_MUSIC import app
from SPOTIFY_MUSIC.utils.database import is_on_off
from config import LOGGER_ID


async def play_logs(message, streamtype):
    if await is_on_off(2):
        logger_text = f"""
<b>{app.mention} бҙҳКҹбҙҖКҸ КҹбҙҸЙў</b>

<b>бҙ„КңбҙҖбҙӣ ЙӘбҙ… :</b> <code>{message.chat.id}</code>
<b>бҙ„КңбҙҖбҙӣ ЙҙбҙҖбҙҚбҙҮ :</b> {message.chat.title}
<b>бҙ„КңбҙҖбҙӣ бҙңsбҙҮКҖЙҙбҙҖбҙҚбҙҮ :</b> @{message.chat.username}

<b>бҙңsбҙҮКҖ ЙӘбҙ… :</b> <code>{message.from_user.id}</code>
<b>ЙҙбҙҖбҙҚбҙҮ :</b> {message.from_user.mention}
<b>бҙңsбҙҮКҖЙҙбҙҖбҙҚбҙҮ :</b> @{message.from_user.username}

<b>З«бҙңбҙҮКҖКҸ :</b> {message.text.split(None, 1)[1]}
<b>sбҙӣКҖбҙҮбҙҖбҙҚбҙӣКҸбҙҳбҙҮ :</b> {streamtype}"""
        if message.chat.id != LOGGER_ID:
            try:
                await app.send_message(
                    chat_id=LOGGER_ID,
                    text=logger_text,
                    parse_mode=ParseMode.HTML,
                    disable_web_page_preview=True,
                )
            except:
                pass
        return